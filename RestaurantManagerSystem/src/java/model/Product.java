package model;

/**
 *
 * @author xuanc
 */
public class Product {
    int idFood;
    String nameFood;
    String category;
    String status;
    int price;

    public Product(int idFood, String nameFood, String category, String status, int price) {
        this.idFood = idFood;
        this.nameFood = nameFood;
        this.category = category;
        this.status = status;
        this.price = price;
    }

    public int getIdFood() {
        return idFood;
    }

    public void setIdFood(int idFood) {
        this.idFood = idFood;
    }

    public String getNameFood() {
        return nameFood;
    }

    public void setNameFood(String nameFood) {
        this.nameFood = nameFood;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
